function M.get_icon(icon_id)
	-- Base glyph offset
	local base_offset = 0xF1F00

	-- Lua 5.3 provides utf8.char, since we don't have it here's some manual codepoint conversion
	local string_char = string.char
	function utf8(cp)
		if cp < 128 then
			return string_char(cp)
		end
		local suffix = cp % 64
		local c4 = 128 + suffix
		cp = (cp - suffix) / 64
		if cp < 32 then
			return string.char(192 + cp, c4)
		end
		suffix = cp % 64
		local c3 = 128 + suffix
		cp = (cp - suffix) / 64
		if cp < 16 then
			return string.char(224 + cp, c3, c4)
		end
		suffix = cp % 64
		cp = (cp - suffix) / 64
		return string.char(240 + cp, 128 + suffix, c3, c4)
	end
	return utf8(base_offset + icon_id) .. " "
end

return M
